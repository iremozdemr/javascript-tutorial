let array = [4,6,2,7,3,1];

console.log(array);

array.sort();
console.log(array);

array.reverse();
console.log(array);

let sorted = array.toSorted();
console.log("sorted array: " + sorted);
console.log(array);

let reversed = array.toReversed();
console.log("reversed array: " + reversed);
console.log(array);